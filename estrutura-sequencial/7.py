"""
7. Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
"""
lado = float(input('Informe o tamanho do lado do quadrado\n'))
area_do_quadrado = lado**2
print(f'A área do quadrado é {area_do_quadrado}\n')
print(f'O dobro da área é {2*area_do_quadrado}\n')
